// @route   POST /api/auth/gdpr-consent
// @desc    Give GDPR consent
// @access  Public (can be used by anonymous users)
router.post('/gdpr-consent', async (req, res) => {
  try {
    const { consent, userId } = req.body;
    const ipAddress = req.ip || req.connection.remoteAddress;
    const userAgent = req.get('User-Agent');

    if (userId) {
      // Registered user
      const user = await User.findById(userId);
      if (!user) {
        return res.status(404).json({
          success: false,
          message: 'User not found'
        });
      }

      if (consent) {
        await user.giveGDPRConsent(ipAddress, userAgent);
      } else {
        await user.revokeGDPRConsent();
      }
    }

    res.json({
      success: true,
      message: `GDPR consent ${consent ? 'granted' : 'revoked'} successfully`,
      consent,
      timestamp: new Date().toISOString()
    });

  } catch (error) {
    console.error('GDPR consent error:', error);
    res.status(500).json({
      success: false,
      message: 'Server error processing GDPR consent'
    });
  }
});

